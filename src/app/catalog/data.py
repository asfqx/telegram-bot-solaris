RENT_SPACE_TARGETS = {item.key for item in RENT_SPACES}


def rent_spaces_for_group(group_key: str) -> tuple[CatalogItem, ...]:
    
    def matches(item: CatalogItem) -> bool:
        
        capacity = item.max_people
        if capacity is None:
            return True
        if group_key == "up_to_10":
            return capacity <= 10
        if group_key == "from_10_to_30":
            return 10 < capacity <= 30
        if group_key == "from_30_to_50":
            return 30 < capacity <= 50
        if group_key == "from_50_to_100":
            return 50 < capacity <= 100
        if group_key == "over_100":
            return capacity > 100
        return False

    return tuple(item for item in RENT_SPACES if matches(item))


